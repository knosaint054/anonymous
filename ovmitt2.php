            // Function to shuffle an array
            function shuffleArray(array) {
                for (let i = array.length - 1; i > 0; i--) {
                    const j = Math.floor(Math.random() * (i + 1));
                    [array[i], array[j]] = [array[j], array[i]];
                }
            }

            // Array of review elements
            const nameElements = document.querySelectorAll('h6.mb-1');

            // Extract names from elements
            const namesArray = Array.from(nameElements).map(nameElement => nameElement.textContent.trim());

            // Shuffle the array of names
            shuffleArray(namesArray);

            // Update the names within the h6 tags
            nameElements.forEach((nameElement, index) => {
                nameElement.textContent = namesArray[index];
            });
        });
